def read_csv_safely(path: Path) -> pd.DataFrame:
    try:
        return pd.read_csv(path, low_memory=False)
    except Exception:
        return pd.read_csv(path, engine="python", low_memory=False)


def find_required_file(path: Path) -> Path:
    if not path.exists():
        raise FileNotFoundError(f"Required file not found: {path}")
    return path


def find_optional_file(paths: list[Path]) -> Path | None:
    for path in paths:
        if path.exists():
            return path
    return None


def safe_mode(x):
    mode_val = x.dropna().mode()
    if len(mode_val) > 0:
        return mode_val.iloc[0]
    return np.nan


def safe_percentile(x, q):
    values = pd.to_numeric(x, errors="coerce").dropna()
    if len(values) == 0:
        return np.nan
    return np.percentile(values, q)


def safe_divide(numerator, denominator, default=0):
    result = numerator / denominator.replace(0, np.nan)
    result = result.replace([np.inf, -np.inf], np.nan)
    return result.fillna(default)


def standardize_year_month(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()

    if "Year" in df.columns:
        df["Year"] = pd.to_numeric(df["Year"], errors="coerce").astype("Int64")

    if "Month" in df.columns:
        df["Month"] = pd.to_numeric(df["Month"], errors="coerce").astype("Int64")

    return df


def fill_numeric(df: pd.DataFrame, cols: list[str], fill_value=0) -> pd.DataFrame:
    df = df.copy()

    for col in cols:
        if col in df.columns:
            df[col] = (
                pd.to_numeric(df[col], errors="coerce")
                .replace([np.inf, -np.inf], np.nan)
                .fillna(fill_value)
            )

    return df


def clean_columns(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()
    df.columns = (
        df.columns.astype(str)
        .str.strip()
        .str.replace(r"\s+", "_", regex=True)
    )
    return df


def normalize_holiday_columns(holidays: pd.DataFrame) -> pd.DataFrame:
    """
    The latest holiday script creates corrected unique-date-level columns.
    This function also keeps compatibility with older alias names if they exist.
    """
    holidays = clean_columns(holidays)

    rename_map = {
        "holiday_record_count": "holiday_classification_count",
        "public_holiday_count": "public_holiday_date_count",
        "bank_holiday_count": "bank_holiday_date_count",
        "mercantile_holiday_count": "mercantile_holiday_date_count",
        "poya_day_count": "poya_day_date_count",
        "weekend_holiday_count": "weekend_holiday_date_count",
        "long_weekend_holiday_count": "long_weekend_holiday_date_count",
        "festive_holiday_count": "festive_holiday_date_count",
    }

    for old_col, new_col in rename_map.items():
        if new_col not in holidays.columns and old_col in holidays.columns:
            holidays[new_col] = holidays[old_col]

    required_holiday_cols = [
        "holiday_date_count",
        "holiday_classification_count",
        "public_holiday_date_count",
        "bank_holiday_date_count",
        "mercantile_holiday_date_count",
        "poya_day_date_count",
        "weekend_holiday_date_count",
        "long_weekend_holiday_date_count",
        "festive_holiday_date_count",
        "holiday_intensity_score",
    ]

    for col in required_holiday_cols:
        if col not in holidays.columns:
            holidays[col] = 0
        holidays[col] = pd.to_numeric(holidays[col], errors="coerce").fillna(0)

    return holidays


def load_population_density() -> pd.DataFrame:
    """
    Reads monthly population density generated by scripts/6.API.py.

    Expected possible columns:
    - location_name or Outlet_ID
    - year / Year
    - month_num / Month or month
    - base_population
    - monthly_density
    - mobility_factor
    - tourism_factor
    - province
    - country
    - pop_source
    """
    density_path = find_optional_file([
        Path("monthly_density.csv"),
        SILVER_DIR / "monthly_density.csv",
        GOLD_DIR / "monthly_density.csv",
    ])

    if density_path is None:
        print("Population density file not found. Gold will continue without density features.")
        return pd.DataFrame(columns=["Outlet_ID"])

    print("Loading population density:", density_path)

    density = read_csv_safely(density_path)
    density = clean_columns(density)

    if "location_name" in density.columns and "Outlet_ID" not in density.columns:
        density = density.rename(columns={"location_name": "Outlet_ID"})

    if "Outlet_ID" not in density.columns:
        raise ValueError("Population density file must contain Outlet_ID or location_name.")

    # Convert possible year/month formats.
    if "Year" not in density.columns and "year" in density.columns:
        density["Year"] = density["year"]

    if "Month" not in density.columns and "month_num" in density.columns:
        density["Month"] = density["month_num"]

    if "Month" not in density.columns and "month" in density.columns:
        parsed_month = pd.to_datetime(density["month"], errors="coerce")
        density["Year"] = parsed_month.dt.year
        density["Month"] = parsed_month.dt.month

    density = standardize_year_month(density)

    numeric_cols = [
        "base_population",
        "monthly_density",
        "mobility_factor",
        "tourism_factor",
    ]

    density = fill_numeric(density, numeric_cols, np.nan)

    if "monthly_density" not in density.columns:
        raise ValueError("Population density file does not contain monthly_density.")

    return density


