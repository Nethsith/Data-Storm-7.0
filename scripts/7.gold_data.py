def clean_input_types(data: dict) -> dict:
    om = clean_columns(data["outlet_master"])
    oc = clean_columns(data["outlet_coordinates"])
    sales = clean_columns(data["sales"])
    returns = clean_columns(data["returns"])
    holidays = normalize_holiday_columns(data["holidays"])
    seasonality = clean_columns(data["seasonality"])
    return_features = clean_columns(data["return_features"])
    billing_features = clean_columns(data["billing_features"])
    context_score = clean_columns(data["context_score"]) if not data["context_score"].empty else data["context_score"]
    density = data["population_density"]

    sales = standardize_year_month(sales)
    returns = standardize_year_month(returns)
    holidays = standardize_year_month(holidays)
    seasonality = standardize_year_month(seasonality)

    if not context_score.empty:
        context_score = standardize_year_month(context_score)

    if not density.empty:
        density = standardize_year_month(density)

    for col in ["Volume_Liters", "Total_Bill_Value", "Price_Per_Liter"]:
        if col in sales.columns:
            sales[col] = pd.to_numeric(sales[col], errors="coerce")
        if col in returns.columns:
            returns[col] = pd.to_numeric(returns[col], errors="coerce")

    if "Cooler_Count" in om.columns:
        om["Cooler_Count"] = pd.to_numeric(om["Cooler_Count"], errors="coerce")

    for col in ["Latitude", "Longitude"]:
        if col in oc.columns:
            oc[col] = pd.to_numeric(oc[col], errors="coerce")

    if "seasonality_score" in seasonality.columns:
        seasonality["seasonality_score"] = pd.to_numeric(
            seasonality["seasonality_score"], errors="coerce"
        ).fillna(0)
    else:
        seasonality["seasonality_score"] = seasonality["Seasonality_Index"].map({
            "Favorable": 1,
            "Moderate": 0,
            "Un-Favorable": -1,
        }).fillna(0)

    if "seasonality_multiplier" in seasonality.columns:
        seasonality["seasonality_multiplier"] = pd.to_numeric(
            seasonality["seasonality_multiplier"], errors="coerce"
        ).fillna(1.0)
    else:
        seasonality["seasonality_multiplier"] = seasonality["Seasonality_Index"].map({
            "Favorable": 1.10,
            "Moderate": 1.00,
            "Un-Favorable": 0.90,
        }).fillna(1.0)

    data["outlet_master"] = om
    data["outlet_coordinates"] = oc
    data["sales"] = sales
    data["returns"] = returns
    data["holidays"] = holidays
    data["seasonality"] = seasonality
    data["return_features"] = return_features
    data["billing_features"] = billing_features
    data["context_score"] = context_score
    data["population_density"] = density

    return data


