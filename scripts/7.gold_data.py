def build_monthly_sales_gold(
    sales: pd.DataFrame,
    holidays: pd.DataFrame,
    seasonality: pd.DataFrame,
    context_score: pd.DataFrame,
    density: pd.DataFrame
):
    monthly_sales = (
        sales
        .groupby(["Outlet_ID", "Year", "Month"])
        .agg(
            monthly_liters=("Volume_Liters", "sum"),
            monthly_bill_value=("Total_Bill_Value", "sum"),
            monthly_sku_count=("SKU_ID", "nunique"),
            monthly_avg_price_per_liter=("Price_Per_Liter", "mean"),
            monthly_transaction_count=("Outlet_ID", "count")
        )
        .reset_index()
    )

    monthly_sales["Month_Index"] = monthly_sales["Year"] * 12 + monthly_sales["Month"]

    latest_month_index = monthly_sales["Month_Index"].max()

    monthly_sales["is_recent_3_months"] = (
        monthly_sales["Month_Index"] >= latest_month_index - 2
    ).astype(int)

    monthly_sales["is_recent_6_months"] = (
        monthly_sales["Month_Index"] >= latest_month_index - 5
    ).astype(int)

    # Main distributor per outlet.
    main_distributor = (
        sales
        .groupby("Outlet_ID")
        .agg(
            Distributor_ID=("Distributor_ID", safe_mode),
            distributor_count=("Distributor_ID", "nunique")
        )
        .reset_index()
    )

    monthly_sales = monthly_sales.merge(
        main_distributor,
        on="Outlet_ID",
        how="left"
    )

    # Merge corrected holiday features.
    holiday_cols = [
        "Year",
        "Month",
        "Year_Month",
        "holiday_date_count",
        "holiday_classification_count",
        "public_holiday_date_count",
        "bank_holiday_date_count",
        "mercantile_holiday_date_count",
        "poya_day_date_count",
        "weekend_holiday_date_count",
        "long_weekend_holiday_date_count",
        "festive_holiday_date_count",
        "holiday_intensity_score",
    ]

    holiday_cols = [c for c in holiday_cols if c in holidays.columns]

    monthly_sales = monthly_sales.merge(
        holidays[holiday_cols],
        on=["Year", "Month"],
        how="left"
    )

    holiday_numeric_cols = [
        "holiday_date_count",
        "holiday_classification_count",
        "public_holiday_date_count",
        "bank_holiday_date_count",
        "mercantile_holiday_date_count",
        "poya_day_date_count",
        "weekend_holiday_date_count",
        "long_weekend_holiday_date_count",
        "festive_holiday_date_count",
        "holiday_intensity_score",
    ]

    monthly_sales = fill_numeric(monthly_sales, holiday_numeric_cols, 0)

    # Merge distributor seasonality.
    seasonality_cols = [
        "Distributor_ID",
        "Year",
        "Month",
        "Seasonality_Index",
        "seasonality_score",
        "seasonality_multiplier",
    ]

    seasonality_cols = [c for c in seasonality_cols if c in seasonality.columns]

    monthly_sales = monthly_sales.merge(
        seasonality[seasonality_cols],
        on=["Distributor_ID", "Year", "Month"],
        how="left"
    )

    monthly_sales["Seasonality_Index"] = monthly_sales["Seasonality_Index"].fillna("Moderate")
    monthly_sales["seasonality_score"] = pd.to_numeric(
        monthly_sales["seasonality_score"], errors="coerce"
    ).fillna(0)
    monthly_sales["seasonality_multiplier"] = pd.to_numeric(
        monthly_sales["seasonality_multiplier"], errors="coerce"
    ).fillna(1.0)

    # Merge distributor-month context score if created by 6.holiday.py.
    if not context_score.empty:
        context_cols = [
            "Distributor_ID",
            "Year",
            "Month",
            "seasonality_multiplier_calibrated",
            "seasonality_multiplier_source",
            "distributor_month_demand_context_score",
            "distributor_month_context_score_0_100",
            "is_favorable_holiday_month",
            "is_unfavorable_holiday_month",
            "is_festive_favorable_month",
        ]

        context_cols = [c for c in context_cols if c in context_score.columns]

        monthly_sales = monthly_sales.merge(
            context_score[context_cols],
            on=["Distributor_ID", "Year", "Month"],
            how="left"
        )

    if "seasonality_multiplier_calibrated" not in monthly_sales.columns:
        monthly_sales["seasonality_multiplier_calibrated"] = monthly_sales["seasonality_multiplier"]

    monthly_sales["seasonality_multiplier_calibrated"] = pd.to_numeric(
        monthly_sales["seasonality_multiplier_calibrated"],
        errors="coerce"
    ).fillna(monthly_sales["seasonality_multiplier"])

    if "seasonality_multiplier_source" not in monthly_sales.columns:
        monthly_sales["seasonality_multiplier_source"] = "seasonality_default"

    if "distributor_month_demand_context_score" not in monthly_sales.columns:
        monthly_sales["distributor_month_demand_context_score"] = (
            monthly_sales["holiday_intensity_score"]
            * monthly_sales["seasonality_multiplier_calibrated"]
        )

    monthly_sales["distributor_month_demand_context_score"] = pd.to_numeric(
        monthly_sales["distributor_month_demand_context_score"],
        errors="coerce"
    ).fillna(
        monthly_sales["holiday_intensity_score"]
        * monthly_sales["seasonality_multiplier_calibrated"]
    )

    monthly_sales["seasonality_adjusted_holiday_score"] = (
        monthly_sales["holiday_intensity_score"]
        * monthly_sales["seasonality_multiplier_calibrated"]
    )

    if "is_favorable_holiday_month" not in monthly_sales.columns:
        monthly_sales["is_favorable_holiday_month"] = (
            (monthly_sales["holiday_intensity_score"] > 0)
            & (monthly_sales["Seasonality_Index"] == "Favorable")
        ).astype(int)

    if "is_unfavorable_holiday_month" not in monthly_sales.columns:
        monthly_sales["is_unfavorable_holiday_month"] = (
            (monthly_sales["holiday_intensity_score"] > 0)
            & (monthly_sales["Seasonality_Index"] == "Un-Favorable")
        ).astype(int)

    if "is_festive_favorable_month" not in monthly_sales.columns:
        monthly_sales["is_festive_favorable_month"] = (
            (monthly_sales["festive_holiday_date_count"] > 0)
            & (monthly_sales["Seasonality_Index"] == "Favorable")
        ).astype(int)

    # Merge monthly population density if available.
    if not density.empty and {"Outlet_ID", "Year", "Month", "monthly_density"}.issubset(density.columns):
        density_cols = [
            "Outlet_ID",
            "Year",
            "Month",
            "base_population",
            "monthly_density",
            "mobility_factor",
            "tourism_factor",
            "province",
        ]

        density_cols = [c for c in density_cols if c in density.columns]

        monthly_sales = monthly_sales.merge(
            density[density_cols],
            on=["Outlet_ID", "Year", "Month"],
            how="left"
        )
    else:
        monthly_sales["base_population"] = np.nan
        monthly_sales["monthly_density"] = np.nan
        monthly_sales["mobility_factor"] = np.nan
        monthly_sales["tourism_factor"] = np.nan
        monthly_sales["province"] = np.nan

    return monthly_sales, main_distributor


