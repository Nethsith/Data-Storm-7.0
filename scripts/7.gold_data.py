def build_outlet_transaction_features(monthly_sales: pd.DataFrame, sales: pd.DataFrame, returns: pd.DataFrame, main_distributor: pd.DataFrame):
    outlet_features = (
        monthly_sales
        .groupby("Outlet_ID")
        .agg(
            avg_monthly_liters=("monthly_liters", "mean"),
            median_monthly_liters=("monthly_liters", "median"),
            max_monthly_liters=("monthly_liters", "max"),
            p75_monthly_liters=("monthly_liters", lambda x: safe_percentile(x, 75)),
            p90_monthly_liters=("monthly_liters", lambda x: safe_percentile(x, 90)),
            sales_std=("monthly_liters", "std"),
            total_liters=("monthly_liters", "sum"),

            avg_bill_value=("monthly_bill_value", "mean"),
            max_bill_value=("monthly_bill_value", "max"),
            total_bill_value=("monthly_bill_value", "sum"),

            avg_sku_count=("monthly_sku_count", "mean"),
            max_sku_count=("monthly_sku_count", "max"),
            avg_transaction_count=("monthly_transaction_count", "mean"),
            total_transaction_count=("monthly_transaction_count", "sum"),
            avg_price_per_liter=("monthly_avg_price_per_liter", "mean"),
            active_months=("monthly_liters", "count"),

            avg_holiday_intensity_score=("holiday_intensity_score", "mean"),
            max_holiday_intensity_score=("holiday_intensity_score", "max"),
            avg_public_holiday_date_count=("public_holiday_date_count", "mean"),
            avg_poya_day_date_count=("poya_day_date_count", "mean"),
            avg_festive_holiday_date_count=("festive_holiday_date_count", "mean"),
            avg_long_weekend_holiday_date_count=("long_weekend_holiday_date_count", "mean"),

            avg_seasonality_score=("seasonality_score", "mean"),
            avg_seasonality_multiplier=("seasonality_multiplier", "mean"),
            avg_seasonality_multiplier_calibrated=("seasonality_multiplier_calibrated", "mean"),
            avg_seasonality_adjusted_holiday_score=("seasonality_adjusted_holiday_score", "mean"),
            max_seasonality_adjusted_holiday_score=("seasonality_adjusted_holiday_score", "max"),

            avg_distributor_month_context_score=("distributor_month_demand_context_score", "mean"),
            max_distributor_month_context_score=("distributor_month_demand_context_score", "max"),

            favorable_holiday_month_count=("is_favorable_holiday_month", "sum"),
            unfavorable_holiday_month_count=("is_unfavorable_holiday_month", "sum"),
            festive_favorable_month_count=("is_festive_favorable_month", "sum"),

            avg_base_population=("base_population", "mean"),
            max_base_population=("base_population", "max"),
            avg_monthly_density=("monthly_density", "mean"),
            max_monthly_density=("monthly_density", "max"),
            avg_mobility_factor=("mobility_factor", "mean"),
            avg_tourism_factor=("tourism_factor", "mean"),
            province=("province", safe_mode),
        )
        .reset_index()
    )

    outlet_features["sales_cv"] = (
        outlet_features["sales_std"]
        / outlet_features["avg_monthly_liters"].replace(0, np.nan)
    ).replace([np.inf, -np.inf], np.nan)

    recent_3 = (
        monthly_sales[monthly_sales["is_recent_3_months"] == 1]
        .groupby("Outlet_ID")
        .agg(
            recent_3_month_avg_liters=("monthly_liters", "mean"),
            recent_3_month_total_liters=("monthly_liters", "sum")
        )
        .reset_index()
    )

    recent_6 = (
        monthly_sales[monthly_sales["is_recent_6_months"] == 1]
        .groupby("Outlet_ID")
        .agg(
            recent_6_month_avg_liters=("monthly_liters", "mean"),
            recent_6_month_total_liters=("monthly_liters", "sum")
        )
        .reset_index()
    )

    january_features = (
        monthly_sales[monthly_sales["Month"] == 1]
        .groupby("Outlet_ID")
        .agg(
            avg_january_liters=("monthly_liters", "mean"),
            max_january_liters=("monthly_liters", "max"),
            avg_january_bill_value=("monthly_bill_value", "mean"),
            avg_january_sku_count=("monthly_sku_count", "mean"),
            avg_january_density=("monthly_density", "mean"),
            max_january_density=("monthly_density", "max"),
            avg_january_context_score=("distributor_month_demand_context_score", "mean"),
            avg_january_adjusted_holiday_score=("seasonality_adjusted_holiday_score", "mean"),
        )
        .reset_index()
    )

    sku_features = (
        sales
        .groupby("Outlet_ID")
        .agg(total_unique_skus=("SKU_ID", "nunique"))
        .reset_index()
    )

    normal_no_apr_dec = (
        monthly_sales[~monthly_sales["Month"].isin([4, 12])]
        .groupby("Outlet_ID")
        .agg(avg_monthly_liters_excluding_apr_dec=("monthly_liters", "mean"))
        .reset_index()
    )

    april_features = (
        monthly_sales[monthly_sales["Month"] == 4]
        .groupby("Outlet_ID")
        .agg(april_avg_liters=("monthly_liters", "mean"))
        .reset_index()
    )

    december_features = (
        monthly_sales[monthly_sales["Month"] == 12]
        .groupby("Outlet_ID")
        .agg(december_avg_liters=("monthly_liters", "mean"))
        .reset_index()
    )

    outlet_features = outlet_features.merge(recent_3, on="Outlet_ID", how="left")
    outlet_features = outlet_features.merge(recent_6, on="Outlet_ID", how="left")
    outlet_features = outlet_features.merge(january_features, on="Outlet_ID", how="left")
    outlet_features = outlet_features.merge(sku_features, on="Outlet_ID", how="left")
    outlet_features = outlet_features.merge(normal_no_apr_dec, on="Outlet_ID", how="left")
    outlet_features = outlet_features.merge(april_features, on="Outlet_ID", how="left")
    outlet_features = outlet_features.merge(december_features, on="Outlet_ID", how="left")
    outlet_features = outlet_features.merge(main_distributor, on="Outlet_ID", how="left")

    outlet_features["april_spike_ratio"] = (
        outlet_features["april_avg_liters"]
        / outlet_features["median_monthly_liters"].replace(0, np.nan)
    ).replace([np.inf, -np.inf], np.nan).fillna(0)

    outlet_features["december_spike_ratio"] = (
        outlet_features["december_avg_liters"]
        / outlet_features["median_monthly_liters"].replace(0, np.nan)
    ).replace([np.inf, -np.inf], np.nan).fillna(0)

    outlet_features["seasonal_peak_liters"] = outlet_features[
        ["april_avg_liters", "december_avg_liters"]
    ].max(axis=1)

    outlet_features["peak_to_median_ratio"] = (
        outlet_features["seasonal_peak_liters"]
        / outlet_features["median_monthly_liters"].replace(0, np.nan)
    ).replace([np.inf, -np.inf], np.nan).fillna(0)

    outlet_features["seasonal_spike_flag"] = (
        outlet_features["peak_to_median_ratio"] >= 1.50
    ).astype(int)

    return_features = build_return_features(returns)

    return outlet_features, return_features


def build_return_features(returns: pd.DataFrame) -> pd.DataFrame:
    if returns.empty:
        return pd.DataFrame(columns=[
            "Outlet_ID",
            "return_record_count",
            "return_liters_abs",
            "return_value_abs",
            "return_month_count",
            "return_sku_count"
        ])

    returns = returns.copy()

    returns["return_liters_abs"] = pd.to_numeric(
        returns["Volume_Liters"], errors="coerce"
    ).abs()

    returns["return_value_abs"] = pd.to_numeric(
        returns["Total_Bill_Value"], errors="coerce"
    ).abs()

    return_features = (
        returns
        .groupby("Outlet_ID")
        .agg(
            return_record_count=("Outlet_ID", "count"),
            return_liters_abs=("return_liters_abs", "sum"),
            return_value_abs=("return_value_abs", "sum"),
            return_month_count=("Month", "nunique"),
            return_sku_count=("SKU_ID", "nunique")
        )
        .reset_index()
    )

    return return_features


