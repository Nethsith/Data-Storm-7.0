def add_january_2026_context(outlet_gold, holidays, seasonality, context_score, density):
    holidays = standardize_year_month(normalize_holiday_columns(holidays))
    seasonality = standardize_year_month(seasonality)

    # 2026 January seasonality, or historical January fallback.
    jan_2026_seasonality = seasonality[
        (seasonality["Year"] == 2026)
        & (seasonality["Month"] == 1)
    ].copy()

    if len(jan_2026_seasonality) > 0:
        jan_2026_seasonality = jan_2026_seasonality[
            ["Distributor_ID", "Seasonality_Index", "seasonality_score", "seasonality_multiplier"]
        ].rename(columns={
            "Seasonality_Index": "jan_2026_seasonality_index",
            "seasonality_score": "jan_2026_seasonality_score",
            "seasonality_multiplier": "jan_2026_seasonality_multiplier"
        })
    else:
        jan_hist = seasonality[seasonality["Month"] == 1].copy()

        jan_2026_seasonality = (
            jan_hist
            .groupby("Distributor_ID")
            .agg(
                jan_2026_seasonality_index=("Seasonality_Index", safe_mode),
                jan_2026_seasonality_score=("seasonality_score", "mean"),
                jan_2026_seasonality_multiplier=("seasonality_multiplier", "mean")
            )
            .reset_index()
        )

    outlet_gold = outlet_gold.merge(
        jan_2026_seasonality,
        on="Distributor_ID",
        how="left"
    )

    outlet_gold["jan_2026_seasonality_index"] = outlet_gold["jan_2026_seasonality_index"].fillna("Moderate")
    outlet_gold["jan_2026_seasonality_score"] = pd.to_numeric(
        outlet_gold["jan_2026_seasonality_score"], errors="coerce"
    ).fillna(0)
    outlet_gold["jan_2026_seasonality_multiplier"] = pd.to_numeric(
        outlet_gold["jan_2026_seasonality_multiplier"], errors="coerce"
    ).fillna(1.0)

    # Use calibrated January context multiplier if context file exists.
    if not context_score.empty:
        context_score = standardize_year_month(context_score)

        jan_context = context_score[
            (context_score["Year"] == 2026)
            & (context_score["Month"] == 1)
        ].copy()

        if len(jan_context) == 0:
            jan_context = context_score[context_score["Month"] == 1].copy()
            jan_context = (
                jan_context
                .groupby("Distributor_ID")
                .agg(
                    jan_2026_seasonality_multiplier_calibrated=("seasonality_multiplier_calibrated", "mean"),
                    jan_2026_distributor_month_context_score=("distributor_month_demand_context_score", "mean")
                )
                .reset_index()
            )
        else:
            jan_context = jan_context[
                [
                    "Distributor_ID",
                    "seasonality_multiplier_calibrated",
                    "distributor_month_demand_context_score"
                ]
            ].rename(columns={
                "seasonality_multiplier_calibrated": "jan_2026_seasonality_multiplier_calibrated",
                "distributor_month_demand_context_score": "jan_2026_distributor_month_context_score"
            })

        outlet_gold = outlet_gold.merge(
            jan_context,
            on="Distributor_ID",
            how="left"
        )
    else:
        outlet_gold["jan_2026_seasonality_multiplier_calibrated"] = np.nan
        outlet_gold["jan_2026_distributor_month_context_score"] = np.nan

    outlet_gold["jan_2026_seasonality_multiplier_used"] = pd.to_numeric(
        outlet_gold["jan_2026_seasonality_multiplier_calibrated"],
        errors="coerce"
    ).fillna(outlet_gold["jan_2026_seasonality_multiplier"])

    # January 2026 holiday proxy.
    jan_holiday_2026 = holidays[
        (holidays["Year"] == 2026)
        & (holidays["Month"] == 1)
    ].copy()

    if len(jan_holiday_2026) > 0:
        jan_proxy = jan_holiday_2026.drop(
            columns=["Year", "Month", "Year_Month"],
            errors="ignore"
        ).mean(numeric_only=True)
    else:
        jan_proxy = (
            holidays[holidays["Month"] == 1]
            .drop(columns=["Year", "Month", "Year_Month"], errors="ignore")
            .mean(numeric_only=True)
        )

    proxy_map = {
        "holiday_intensity_score": "jan_2026_holiday_intensity_proxy",
        "holiday_date_count": "jan_2026_holiday_date_count_proxy",
        "public_holiday_date_count": "jan_2026_public_holiday_date_count_proxy",
        "poya_day_date_count": "jan_2026_poya_day_date_count_proxy",
        "festive_holiday_date_count": "jan_2026_festive_holiday_date_count_proxy",
        "long_weekend_holiday_date_count": "jan_2026_long_weekend_holiday_date_count_proxy",
    }

    for source_col, target_col in proxy_map.items():
        outlet_gold[target_col] = float(jan_proxy.get(source_col, 0))

    outlet_gold["jan_2026_adjusted_holiday_score_proxy"] = (
        outlet_gold["jan_2026_holiday_intensity_proxy"]
        * outlet_gold["jan_2026_seasonality_multiplier_used"]
    )

    # January 2026 population-density context.
    if not density.empty and {"Outlet_ID", "Year", "Month", "monthly_density"}.issubset(density.columns):
        density = standardize_year_month(density)

        jan_2026_density = density[
            (density["Year"] == 2026)
            & (density["Month"] == 1)
        ].copy()

        if len(jan_2026_density) > 0:
            jan_density_features = (
                jan_2026_density
                .groupby("Outlet_ID")
                .agg(
                    jan_2026_monthly_density=("monthly_density", "mean"),
                    jan_2026_base_population=("base_population", "mean"),
                    jan_2026_mobility_factor=("mobility_factor", "mean"),
                    jan_2026_tourism_factor=("tourism_factor", "mean"),
                )
                .reset_index()
            )
        else:
            jan_density_features = (
                density[density["Month"] == 1]
                .groupby("Outlet_ID")
                .agg(
                    jan_2026_monthly_density=("monthly_density", "mean"),
                    jan_2026_base_population=("base_population", "mean"),
                    jan_2026_mobility_factor=("mobility_factor", "mean"),
                    jan_2026_tourism_factor=("tourism_factor", "mean"),
                )
                .reset_index()
            )

        outlet_gold = outlet_gold.merge(
            jan_density_features,
            on="Outlet_ID",
            how="left"
        )
    else:
        outlet_gold["jan_2026_monthly_density"] = np.nan
        outlet_gold["jan_2026_base_population"] = np.nan
        outlet_gold["jan_2026_mobility_factor"] = np.nan
        outlet_gold["jan_2026_tourism_factor"] = np.nan

    return outlet_gold


