def build_outlet_gold(outlet_master, outlet_coordinates, outlet_features, return_features, billing_features):
    outlet_gold = outlet_master.merge(
        outlet_coordinates,
        on="Outlet_ID",
        how="left",
        suffixes=("", "_coord")
    )

    outlet_gold = outlet_gold.merge(
        outlet_features,
        on="Outlet_ID",
        how="left"
    )

    outlet_gold = outlet_gold.merge(
        return_features,
        on="Outlet_ID",
        how="left"
    )

    if billing_features.empty or "Outlet_ID" not in billing_features.columns:
        billing_features = pd.DataFrame(columns=[
            "Outlet_ID",
            "billing_anomaly_count",
            "zero_volume_positive_bill_count",
            "positive_volume_zero_bill_count",
            "zero_volume_zero_bill_count"
        ])

    outlet_gold = outlet_gold.merge(
        billing_features,
        on="Outlet_ID",
        how="left"
    )

    return outlet_gold


